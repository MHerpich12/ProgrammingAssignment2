## makeCacheMatrix creates "vector" of functions in order to create a matrix,
## return the matrix, create an inverse of the matrix, and return an inverse
## of the matrix.  This will work in tandem with cacheSolve to evaluate the inverse
## of a matrix.  If the inverse has been calculated already, the function call will
## return the cached value.

makeCacheMatrix <- function(x = matrix()) {
  i <- NULL
  set <- function(y) {
    x <<- y
    i <<- NULL
  }
  get <- function() x
  setinv <- function(inv) i <<- inv
  getinv <- function() i
  list(set = set, get = get,
       setinv = setinv,
       getinv = getinv)
}

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
  i <- x$getinv()
  if(!is.null(i)) {
    message("getting cached data")
    return(i)
  }
  data <- x$get()
  i <- solve(data, ...)
  x$setinv(i)
  i
}
